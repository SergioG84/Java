public class ComputeArea {
  public static void main (String[]args){
    double length = 20;
    double width = 2;
    double area;

    area = length * width;

    System.out.println("The length of the rectangle is: " + length);
    System.out.println("The width is: " + width);
    System.out.println("Therefore the area is: " + area);
  }
}
